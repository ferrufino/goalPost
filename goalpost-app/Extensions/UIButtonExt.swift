import UIKit

extension UIButton {
    
    func setSelectColor() {
        self.backgroundColor = #colorLiteral(red: 0, green: 0.6195197105, blue: 0.09968512505, alpha: 1)
    }
    
    func setDeselecteColor(){
        self.backgroundColor = #colorLiteral(red: 0.4666666687, green: 0.7647058964, blue: 0.2666666806, alpha: 1)

    }
}
